#!/usr/bin/env bash
# Run the 9-row eval test matrix from
# /Users/andreassjodin/.claude/plans/make-a-evaluation-plan-async-yeti.md
# against the current dev branch (or a re-rebased fix branch).
#
# Each row writes:
#   <EVIDENCE_DIR>/<row>/config.yaml         GUI config snapshot
#   <EVIDENCE_DIR>/<row>/trace_tail.txt      last 100 lines of nextflow trace
#   <EVIDENCE_DIR>/<row>/verdict.txt         pass / fail with one-line reason
#
# Rows 7 and 8 are GUI-only (Bug A reproduction); rows 1-6 and 9 invoke
# nanometanf. Re-running the same row reuses the work cache (-resume) so
# rebuilds are cheap once the conda envs exist.
#
# Usage:
#   ./run_matrix.sh           # run rows 1..9
#   ./run_matrix.sh 2 3 7     # run a subset

set -euo pipefail

REPO=/Users/andreassjodin/Code/nanometa_live-eval
FIXTURES=/Users/andreassjodin/Desktop/snabbsekvensering/output-live/fixtures
RUN_BASE=/Users/andreassjodin/Desktop/snabbsekvensering/output-live/eval-runs
KRAKEN_DB=/Users/andreassjodin/Desktop/snabbsekvensering/bioshield/kraken_db/k2_pluspfp_08_GB_20251015
PIPELINE_SOURCE=/Users/andreassjodin/Code/nanometanf
EVIDENCE_DIR=${REPO}/docs/archive/eval-2026-05-08

mkdir -p "${RUN_BASE}" "${EVIDENCE_DIR}"

source /Users/andreassjodin/miniforge3/etc/profile.d/conda.sh

write_config() {
    # $1=path  $2=input  $3=handling  $4=mode  $5=outdir
    cat > "$1" <<EOF
# Generated by run_matrix.sh
nanopore_output_directory: "$2"
results_output_directory: "$5"
kraken_db: "${KRAKEN_DB}"
processing_mode: "$4"
sample_handling: "$3"
pipeline_profile: "conda"
pipeline_source: "${PIPELINE_SOURCE}"
blast_validation: false
update_interval_seconds: 30
EOF
}

run_pipeline() {
    # $1=row label  $2=config path  $3=outdir
    local row=$1; local cfg=$2; local outdir=$3
    local rowdir="${EVIDENCE_DIR}/${row}"
    mkdir -p "${rowdir}"
    cp "${cfg}" "${rowdir}/config.yaml"
    echo "[${row}] launching nanometa pipeline -> ${outdir}"
    conda activate nanometa
    if nanometa-live run --config "${cfg}" >"${rowdir}/run.log" 2>&1; then
        echo "[${row}] pipeline OK"
        echo "PASS: pipeline finished" > "${rowdir}/verdict.txt"
    else
        echo "[${row}] pipeline FAILED (see run.log)"
        echo "FAIL: pipeline exit non-zero" > "${rowdir}/verdict.txt"
    fi
    if [[ -f "${outdir}/logs/trace.txt" ]]; then
        tail -100 "${outdir}/logs/trace.txt" > "${rowdir}/trace_tail.txt"
    elif [[ -f "${outdir}/pipeline_info/execution_trace.txt" ]]; then
        tail -100 "${outdir}/pipeline_info/execution_trace.txt" > "${rowdir}/trace_tail.txt"
    else
        echo "(no trace file found)" > "${rowdir}/trace_tail.txt"
    fi
}

row1_batch_by_barcode() {
    local out="${RUN_BASE}/row1"
    local cfg="${RUN_BASE}/row1.config.yaml"
    write_config "${cfg}" "${FIXTURES}/multiplex-bc5" "by_barcode" "batch" "${out}"
    run_pipeline "row1-batch-by_barcode" "${cfg}" "${out}"
}

row2_batch_per_file() {
    local out="${RUN_BASE}/row2"
    local cfg="${RUN_BASE}/row2.config.yaml"
    write_config "${cfg}" "${FIXTURES}/per-file-flat" "per_file" "batch" "${out}"
    run_pipeline "row2-batch-per_file" "${cfg}" "${out}"
}

row3_batch_single_sample() {
    local out="${RUN_BASE}/row3"
    local cfg="${RUN_BASE}/row3.config.yaml"
    write_config "${cfg}" "${FIXTURES}/singleplex-flat" "single_sample" "batch" "${out}"
    run_pipeline "row3-batch-single_sample" "${cfg}" "${out}"
}

row4_batch_samplesheet() {
    local out="${RUN_BASE}/row4"
    local cfg="${RUN_BASE}/row4.config.yaml"
    # nanometa-live picks up the samplesheet via nanopore_output_directory
    # pointing at the multiplex tree -- the samplesheet itself is a CSV
    # the GUI (or this row) hands to nextflow as --input. We emit the CSV
    # next to the config so the row records both.
    write_config "${cfg}" "${FIXTURES}/multiplex-bc5" "by_barcode" "batch" "${out}"
    cp "${FIXTURES}/samplesheet/samplesheet.csv" "${EVIDENCE_DIR}/row4-batch-samplesheet/samplesheet.csv" 2>/dev/null || true
    run_pipeline "row4-batch-samplesheet" "${cfg}" "${out}"
}

row5_realtime_by_barcode() {
    local out="${RUN_BASE}/row5"
    local cfg="${RUN_BASE}/row5.config.yaml"
    local watch="${RUN_BASE}/row5_watch"
    rm -rf "${watch}" && mkdir -p "${watch}"
    write_config "${cfg}" "${watch}" "by_barcode" "realtime" "${out}"
    # Stage nanorunner replay against ${watch} in the background while the
    # pipeline runs; replay copies barcode dirs into watch with a small
    # interval so watchPath fires.
    conda run -n nanorunner nanorunner replay \
        --source "${FIXTURES}/multiplex-bc5" --target "${watch}" \
        --interval 1 --batch-size 4 --force-structure multiplex >"${RUN_BASE}/row5.replay.log" 2>&1 &
    local replay_pid=$!
    run_pipeline "row5-realtime-by_barcode" "${cfg}" "${out}"
    kill "${replay_pid}" 2>/dev/null || true
}

